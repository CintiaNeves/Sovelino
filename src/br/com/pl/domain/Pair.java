package br.com.pl.domain;

/**
 * Classe que representa os pontos
 * encotrados que serao plotados
 * no grafico ( plano cartesiano )
 * 
 *
 */
public class Pair {
	
	/*
	 * Coordenada X
	 */
	private Double x;
	
	/*
	 * Coordenada Y
	 */
	private Double y;

	public Double getX() {
		return x;
	}
	public void setX(Double x) {
		this.x = x;
	}
	public Double getY() {
		return y;
	}
	public void setY(Double y) {
		this.y = y;
	}
	
	

}